package day7.staticMethods;

public class MethodSyntaxExamples {
	// static global variables
	static int age;
	static double salary;	
	/**
	 * This is a static method with return type as int and no parameter in it
	 * Method type: 
	 * Access Modifier: 
	 * non-access Modifier: 
	 * return type: 
	 * method name: 
	 * argument:  
	 * return value: 
	 */
	public static int test1() {
		System.out.println("I am test1 method.");
		//logic		
//		int pin=201201;
//		return pin;
		return 201201;
	}
	/**
	 * This is a static method with return type as void and having no parameter
	 * Method type: 
	 * Access Modifier: 
	 * non-access Modifier: 
	 * return type: 
	 * method name: 
	 * argument:  
	 * return value: 
	 */
	static void test2() {
		System.out.println("I am test2 method");
		//return; this will be written by java compiler at compile
	}
	/**
	 * This is a static method with return type as double and having parameter as boolean
	 * Method type: 
	 * Access Modifier: 
	 * non-access Modifier: 
	 * return type: 
	 * method name: 
	 * argument:  
	 * return value: 
	 */
	static public double test3(boolean b1) {
		System.out.println("");
		double d=15.36;
		return d;
	}
	
	/**
	 * This is a static private method with return type as void and having parameter as float
	 * Method type: 
	 * Access Modifier: 
	 * non-access Modifier: 
	 * return type: 
	 * method name: 
	 * argument:  
	 * return value: 
	 */
	static private void test4(float f) {
		System.out.println("");
	}
	/**
	 * Method type: 
	 * Access Modifier: 
	 * non-access Modifier: 
	 * return type: 
	 * method name: 
	 * argument:  
	 * return value: 
	 */
	static public void main(String[] args) 
	{
		

	}

}
