package day5.loops;

public class ForLoop2 {
	public static void main(String args[]) {

		for (char c1 = 'a'; c1 <= 'z'; c1++) {
			System.out.print(c1 + " ");
		}
		System.out.println("\n*****Print in CAP*********");

		for (char c1 = 'A'; c1 <= 'Z'; c1++) {
			System.out.print(c1 + " ");
		}
	}
}
